// There are 7 primitive data types:
//
// Number: Floating point numbers - used for decimals and integers
// Strings: Sequence of characters - used for text
// Boolean: Logical type that can only be true or false
// Undefined: Value taken by a variable that is not yet defined (empty value)
// Null: Also means empty value
// Symbol: Value that is unique and cannot be changed
// BigInt: Larger integers than the Number type can hold

// You can re-assign values to a variable through the code and it will
// automatically pick up a change in data type
let jsIsFun = true;
console.log(typeof jsIsFun);

let jsIsFun = "True";
console.log(typeof jsIsFun);
