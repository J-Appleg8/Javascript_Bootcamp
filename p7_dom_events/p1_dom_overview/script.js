// Document Object Model:
// - Structured representation of HTML documents
// - Allows JavaScript to access HTML elements and styles to manipulate them
// - DOM is automatically created as soon as the browser loads

// The 'document' object is an entry point to the DOM
// example: document.querySelector()

// The DOM is a complete representation of the HTML document

// The DOM Tree Structure:
// <html>
//   <head>
//     <title></title>
//   </head>
//   <body>
//     <main>
//       <section>
//         <p>Start guessing...</p>
//         <p>20</p>
//       </section>
//     </main>
//   </body>
// </html>

// For each element in the DOM tree, there is an element node that Javascript can access
